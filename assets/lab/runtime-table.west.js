  function line(key, value) {
    if (value === undefined || value === null) return key + "=";
    if (Array.isArray(value) || isObject(value)) {
      try {
        return key + "=" + JSON.stringify(value);
      } catch (_error) {
        return key + "=UNSERIALIZABLE";
      }
    }
    return key + "=" + String(value);
  }

