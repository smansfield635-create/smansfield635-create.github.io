  function runDirect(
    role,
    payload
  ) {
    var manifestEntry =
      PARTICIPANT_MANIFEST.filter(
        function matchRole(entry) {
          return (
            entry.role ===
            role
          );
        }
      )[0] || null;

    if (
      !manifestEntry ||
      manifestEntry.auxiliary
    ) {
      return freeze({
        status:
          "HELD",

        role:
          role,

        reason:
          "DIRECT_ROLE_NOT_AUTHORIZED",

        receipt:
          null
      });
    }

    var target =
      resolveCallableParticipant(
        manifestEntry
      );

    if (!target) {
      return freeze({
        status:
          "MISSING",

        role:
          role,

        reason:
          "PARTICIPANT_NOT_AVAILABLE",

        receipt:
          null
      });
    }

    var method = null;

    if (
      typeof target ===
      "function"
    ) {
      method =
        target;
    } else {
      (
        manifestEntry.methods ||
        []
      ).some(
        function findMethod(name) {
          if (
            target &&
            typeof target[name] ===
              "function"
          ) {
            method =
              target[name]
                .bind(target);

            return true;
          }

          return false;
        }
      );
    }

    if (!method) {
      return freeze({
        status:
          "HELD",

        role:
          role,

        reason:
          "PARTICIPANT_NOT_CALLABLE",

        receipt:
          null
      });
    }

    try {
      var result =
        method(
          payload || {}
        );

      state.directExecutions += 1;

      var receipt =
        freeze({
          schema:
            "AUDRALIA_DIAGNOSTIC_DIRECT_EXECUTION_RECEIPT_v1",

          role:
            role,

          status:
            "AVAILABLE",

          result:
            clone(result),

          createdAt:
            nowIso()
        });

      archiveRecord({
        schema:
          ARCHIVE_SCHEMA,

        recordType:
          "DIRECT_EXECUTION",

        receipt:
          receipt,

        archivedAt:
          nowIso()
      });

      recordAction(
        "runDirect",
        {
          role:
            role,

          status:
            receipt.status
        }
      );

      return receipt;
    } catch (error) {
      var failure =
        freeze({
          schema:
            "AUDRALIA_DIAGNOSTIC_DIRECT_EXECUTION_RECEIPT_v1",

          role:
            role,

          status:
            "ERROR",

          error:
            normalizeError(
              error,
              "runDirect." +
              role
            ),

          createdAt:
            nowIso()
        });

      archiveRecord({
        schema:
          ARCHIVE_SCHEMA,

        recordType:
          "DIRECT_EXECUTION_ERROR",

        receipt:
          failure,

        archivedAt:
          nowIso()
      });

      return failure;
    }
  }

