  function validateExactNine(
    normalizedReceipts
  ) {
    var receipts =
      Array.isArray(
        normalizedReceipts
      )
        ? normalizedReceipts
        : [];

    var exactCount =
      receipts.length === 9;

    var oneCycleId =
      exactCount &&
      receipts.every(
        function hasCycleId(receipt) {
          return Boolean(
            receipt.cycleId
          );
        }
      ) &&
      uniqueStrings(
        receipts.map(
          function cycleId(receipt) {
            return receipt.cycleId;
          }
        )
      ).length === 1;

    var canonicalOrder =
      exactCount &&
      receipts.every(
        function canonicalReceipt(
          receipt,
          index
        ) {
          return Boolean(
            receipt &&
            receipt.validation &&
            receipt.validation
              .validCanonicalCoordinate
          );
        }
      );

    var completed =
      exactCount &&
      receipts.every(
        function completedReceipt(
          receipt
        ) {
          return Boolean(
            receipt.completed
          );
        }
      );

    var handoffEligible =
      exactCount &&
      receipts.every(
        function eligibleReceipt(
          receipt
        ) {
          return Boolean(
            receipt.handoffEligible
          );
        }
      );

    var noFailure =
      exactCount &&
      receipts.every(
        function noFailureReceipt(
          receipt
        ) {
          var status =
            String(
              receipt.status ||
              "UNKNOWN"
            ).toUpperCase();

          return Boolean(
            status !== "FAIL" &&
            status !== "FAILED" &&
            status !== "ERROR" &&
            status !== "CONFLICT" &&
            status !== "HOLD" &&
            status !== "HELD"
          );
        }
      );

    return freeze({
      exactCount:
        exactCount,

      oneCycleId:
        oneCycleId,

      canonicalOrder:
        canonicalOrder,

      completed:
        completed,

      handoffEligible:
        handoffEligible,

      noFailure:
        noFailure,

      exactNineValidated:
        Boolean(
          exactCount &&
          oneCycleId &&
          canonicalOrder &&
          completed &&
          handoffEligible &&
          noFailure
        )
    });
  }

