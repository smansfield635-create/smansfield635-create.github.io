  function classifyCycleTerminal(
    normalizedReceipts,
    exactValidation,
    restitution
  ) {
    var hasError =
      normalizedReceipts.some(
        function errorReceipt(receipt) {
          var status =
            String(
              receipt.status ||
              ""
            ).toUpperCase();

          return Boolean(
            status === "ERROR" ||
            status === "FAIL" ||
            status === "FAILED"
          );
        }
      );

    if (hasError) {
      return "ERROR_TERMINAL";
    }

    if (
      exactValidation
        .exactNineValidated
    ) {
      return "COMMITTED_EXACT_NINE";
    }

    if (
      restitution
        .firstHeldCoordinate ||
      restitution
        .firstConflictCoordinate ||
      restitution
        .missingStations.length
    ) {
      return "REPORTABLE_LIMITED_DIAGNOSTIC";
    }

    return "REPORTABLE_LIMITED_DIAGNOSTIC";
  }

  function runNineCycle(payload) {
    var conductorEntry =
      PARTICIPANT_MANIFEST.filter(
        function conductor(entry) {
          return (
            entry.role ===
            "NORTH_CONDUCTOR"
          );
        }
      )[0];

    var conductor =
      resolveCallableParticipant(
        conductorEntry
      );

    if (
      !conductor ||
      typeof conductor.runNineCycle !==
        "function"
    ) {
      var missingConductor =
        freeze({
          schema:
            CYCLE_RECEIPT_SCHEMA,

          status:
            "HELD",

          terminalClass:
            "REPORTABLE_LIMITED_DIAGNOSTIC",

          reason:
            "NORTH_CONDUCTOR_UNAVAILABLE",

          receiptCount:
            0,

          exactNineValidated:
            false,

          sequentialFallbackAuthorized:
            false,

          receipts:
            [],

          normalizedReceipts:
            [],

          restitution: {
            firstHeldCoordinate:
              "F1:NORTH_CONDUCTOR",

            firstFailedCoordinate:
              null,

            firstConflictCoordinate:
              null,

            missingStations:
              FIBONACCI_STATIONS.map(
                function stationRole(station) {
                  return station.role;
                }
              ),

            recommendedOwner: {
              ownerType:
                "DIAGNOSTIC_PARTICIPANT",

              subjectId:
                "NORTH_CONDUCTOR",

              contract:
                null,

              file:
                "/assets/audralia/audralia.diagnostic.north.conductor.js",

              component:
                "NORTH_CONDUCTOR"
            },

            recommendedAction:
              "LOAD_NORTH_CONDUCTOR",

            falseContinuation:
              false,

            sequentialFallbackAuthorized:
              false
          },

          createdAt:
            nowIso()
        });

      state.currentCycleReceipt =
        missingConductor;

      archiveCycle(
        null,
        missingConductor
      );

      return missingConductor;
    }

    var canonicalRequest;

    try {
      canonicalRequest =
        buildCanonicalCycleRequest(
          payload || {}
        );
    } catch (requestError) {
      var requestFailure =
        freeze({
          schema:
            CYCLE_RECEIPT_SCHEMA,

          status:
            "ERROR",

          terminalClass:
            "ERROR_TERMINAL",

          reason:
            "CANONICAL_CYCLE_REQUEST_ERROR",

          error:
            normalizeError(
              requestError,
              "buildCanonicalCycleRequest"
            ),

          receiptCount:
            0,

          exactNineValidated:
            false,

          sequentialFallbackAuthorized:
            false,

          receipts:
            [],

          normalizedReceipts:
            [],

          createdAt:
            nowIso()
        });

      state.currentCycleReceipt =
        requestFailure;

      archiveCycle(
        null,
        requestFailure
      );

      return requestFailure;
    }

    state.currentCycleRequest =
      canonicalRequest;

    var rawResult;

    try {
      rawResult =
        conductor.runNineCycle(
          canonicalRequest
        );
    } catch (error) {
      var conductorFailure =
        freeze({
          schema:
            CYCLE_RECEIPT_SCHEMA,

          status:
            "ERROR",

          terminalClass:
            "ERROR_TERMINAL",

          reason:
            "NORTH_CONDUCTOR_EXECUTION_ERROR",

          error:
            normalizeError(
              error,
              "runNineCycle"
            ),

          canonicalRequest:
            frozenClone(
              canonicalRequest
            ),

          receiptCount:
            0,

          exactNineValidated:
            false,

          sequentialFallbackAuthorized:
            false,

          receipts:
            [],

          normalizedReceipts:
            [],

          createdAt:
            nowIso()
        });

      state.currentCycleReceipt =
        conductorFailure;

      archiveCycle(
        canonicalRequest,
        conductorFailure
      );

      return conductorFailure;
    }

    state.cycleExecutions += 1;

    var rawReceipts =
      rawResult &&
      Array.isArray(
        rawResult.receipts
      )
        ? rawResult.receipts
        : Array.isArray(rawResult)
          ? rawResult
          : [];

    var normalizedReceipts =
      normalizeCycleReceipts(
        rawReceipts
      );

    var exactValidation =
      validateExactNine(
        normalizedReceipts
      );

    var restitution =
      deriveCycleRestitution(
        normalizedReceipts,
        exactValidation,
        canonicalRequest
      );

    var terminalClass =
      classifyCycleTerminal(
        normalizedReceipts,
        exactValidation,
        restitution
      );

    var cycleStatus =
      exactValidation
        .exactNineValidated
        ? "COMMITTED"
        : terminalClass ===
          "ERROR_TERMINAL"
          ? "ERROR"
          : "HELD";

    var cycleReceipt =
      freeze({
        schema:
          CYCLE_RECEIPT_SCHEMA,

        status:
          cycleStatus,

        terminalClass:
          terminalClass,

        cycleId:
          canonicalRequest.cycleId,

        requestHash:
          canonicalRequest
            .requestHash,

        receiptCount:
          rawReceipts.length,

        normalizedReceiptCount:
          normalizedReceipts
            .length,

        exactNineValidated:
          exactValidation
            .exactNineValidated,

        exactNineValidation:
          exactValidation,

        sequentialFallbackAuthorized:
          false,

        canonicalRequest:
          frozenClone(
            canonicalRequest
          ),

        receipts:
          frozenClone(
            rawReceipts
          ),

        normalizedReceipts:
          normalizedReceipts,

        restitution:
          restitution,

        readinessClaimed:
          false,

        visualPassClaimed:
          false,

        repairAuthorized:
          false,

        createdAt:
          nowIso()
      });

    if (
      exactValidation
        .exactNineValidated
    ) {
      state.cycleCommits += 1;
    }

    state.currentCycleReceipt =
      cycleReceipt;

    archiveCycle(
      canonicalRequest,
      cycleReceipt
    );

    root.AUDRALIA_DROP_WITH_READ_DIAGNOSTIC_CYCLE_RECEIPT =
      cycleReceipt;

    publishEngineReceipt();

    recordAction(
      "runNineCycle",
      {
        cycleId:
          cycleReceipt.cycleId,

        status:
          cycleReceipt.status,

        terminalClass:
          cycleReceipt
            .terminalClass,

        receiptCount:
          cycleReceipt
            .receiptCount,

        exactNineValidated:
          cycleReceipt
            .exactNineValidated,

        firstHeldCoordinate:
          cycleReceipt
            .restitution
            .firstHeldCoordinate
      }
    );

    return frozenClone(
      cycleReceipt
    );
  }

  function renderReport(
    report,
    receipt
  ) {
    if (!doc) {
      return false;
    }

    var rootElement =
      doc.getElementById(
        "diagnosticReport"
      ) ||
      doc.querySelector(
        "[data-diagnostic-report]"
      );

    if (!rootElement) {
      return false;
    }

    var statusElement =
      rootElement.querySelector(
        "[data-report-status]"
      );

    var resultElement =
      rootElement.querySelector(
        "[data-read-result]"
      );

    var evidenceElement =
      rootElement.querySelector(
        "[data-read-evidence]"
      );

    var absenceElement =
      rootElement.querySelector(
        "[data-read-absence]"
      );

    var directionElement =
      rootElement.querySelector(
        "[data-read-direction]"
      );

    var receiptElement =
      rootElement.querySelector(
        "[data-report-receipt]"
      );

    if (statusElement) {
      statusElement.textContent =
        report.status;
    }

    if (resultElement) {
      resultElement.textContent =
        report.READ.Result;
    }

    renderList(
      evidenceElement,
      report.READ.Evidence
    );

    renderList(
      absenceElement,
      report.READ.Absence
    );

    renderList(
      directionElement,
      report.READ.Direction
    );

    if (receiptElement) {
      receiptElement.textContent =
        JSON.stringify(
          receipt,
          null,
          2
        );
    }

    rootElement.hidden =
      false;

    rootElement.setAttribute(
      "data-report-state",
      String(report.status)
        .toLowerCase()
    );

    return true;
  }

  function renderList(
    element,
    values
  ) {
    if (!element || !doc) {
      return;
    }

    element.textContent = "";

    (
      Array.isArray(values)
        ? values
        : []
    ).forEach(
      function renderValue(value) {
        var item =
          doc.createElement("li");

        item.textContent =
          String(value);

        element.appendChild(item);
      }
    );
  }

