  function runCycleClosureAudit() {
    state.runCount += 1;
    state.timestamp = nowIso();

    try {
      inspectAllNodes();
      inspectRelationships();

      const failure = resolveFirstOpenDuty();

      if (failure.coordinate === "NONE") {
        applyRelease();
      } else {
        applyHold(failure);
      }

      updateDataset();
      publishGlobals();

      return getReceipt();
    } catch (error) {
      state.errorCount += 1;
      state.errors.push({
        at: nowIso(),
        message: safeString(error && error.message ? error.message : error)
      });

      applyHold({
        coordinate: "LAB_WEST_DUAL_CHAPEL_METRIC_AUDIT_ERROR",
        failureClass: WEST_GAP_CLASS.STRUCTURAL_BLOCK,
        reason: safeString(error && error.message ? error.message : error),
        file: FILE,
        owner: "LAB_WEST"
      });

      updateDataset();
      publishGlobals();

      return getReceipt();
    }
  }

