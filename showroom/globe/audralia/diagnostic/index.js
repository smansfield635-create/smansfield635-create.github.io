  function clone(value, seen) {
    var memory =
      seen || [];

    if (
      value === null ||
      value === undefined ||
      typeof value === "string" ||
      typeof value === "number" ||
      typeof value === "boolean"
    ) {
      return value;
    }

    if (typeof value === "bigint") {
      return value.toString();
    }

    if (typeof value === "function") {
      return {
        type: "Function",
        name:
          value.name ||
          "anonymous"
      };
    }

    if (
      memory.indexOf(value) !== -1
    ) {
      return "[Circular]";
    }

    memory.push(value);

    if (Array.isArray(value)) {
      return value.map(
        function cloneItem(item) {
          return clone(
            item,
            memory.slice()
          );
        }
      );
    }

    var output = {};

    Object.keys(value)
      .forEach(
        function cloneKey(key) {
          try {
            output[key] =
              clone(
                value[key],
                memory.slice()
              );
          } catch (error) {
            output[key] = {
              unreadable: true,
              message:
                error &&
                error.message
                  ? error.message
                  : String(error)
            };
          }
        }
      );

    return output;
  }

