  function callPacket(authority, methods) {
    if (!authority) {
      return {
        method: "NONE",
        packet: null,
        attempted: false,
        error: "NO_AUTHORITY"
      };
    }

    for (var i = 0; i < methods.length; i += 1) {
      var method = methods[i];
      if (!isFunction(authority[method])) continue;

      var call = callMethod(authority, method);

      if (!call.ok) {
        return {
          method: method,
          packet: null,
          attempted: true,
          error: call.error
        };
      }

      if (isObject(call.output)) {
        return {
          method: method,
          packet: clone(call.output),
          attempted: true,
          error: "NONE"
        };
      }

      if (typeof call.output === "string" && call.output.trim()) {
        return {
          method: method,
          packet: { statusText: call.output },
          attempted: true,
          error: "NONE"
        };
      }
    }

    return {
      method: "NONE",
      packet: null,
      attempted: false,
      error: "NO_OBJECT_PACKET_RETURNED"
    };
  }

