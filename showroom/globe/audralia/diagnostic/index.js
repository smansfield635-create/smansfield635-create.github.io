  function resolveIdentityField(
    fieldName,
    candidates
  ) {
    var usable =
      (
        Array.isArray(candidates)
          ? candidates
          : []
      ).filter(Boolean);

    var selected =
      usable.length
        ? usable[0]
        : null;

    var conflicts = [];

    if (selected) {
      usable.slice(1)
        .forEach(
          function compareCandidate(candidate) {
            if (
              candidate.value !==
              selected.value
            ) {
              conflicts.push({
                field:
                  fieldName,

                selected:
                  clone(selected),

                conflicting:
                  clone(candidate)
              });
            }
          }
        );
    }

    if (conflicts.length) {
      state.identityConflicts =
        state.identityConflicts
          .concat(conflicts)
          .slice(-64);
    }

    if (!selected) {
      state.identityAbsence.push({
        field:
          fieldName,

        status:
          "UNAVAILABLE",

        observedAt:
          nowIso()
      });

      state.identityAbsence =
        state.identityAbsence
          .slice(-64);
    }

    return freeze({
      schema:
        IDENTITY_RECORD_SCHEMA,

      field:
        fieldName,

      value:
        selected
          ? selected.value
          : null,

      status:
        selected
          ? conflicts.length
            ? "CONFLICT"
            : "AVAILABLE"
          : "UNAVAILABLE",

      source:
        selected
          ? selected.source
          : "UNAVAILABLE",

      evidenceKind:
        selected
          ? selected.evidenceKind
          : "ABSENT",

      detail:
        selected
          ? clone(selected.detail)
          : null,

      conflicts:
        conflicts,

      precedence:
        IDENTITY_PRECEDENCE.slice()
    });
  }

